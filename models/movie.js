const { mongoose } = require('mongoose');
// const bcrypt = require('bcryptjs');

const movieSchema = new mongoose.Schema({
  country: {
    type: String,
    required: true,
  },
  director: {
    type: String,
    required: true,
  },
  duration: {
    type: Number,
    required: true,
  },
  year: {
    type: String,
    required: true,
  },
  description: {
    type: String,
    required: true,
  },
  image: {
    type: String,
    required: true,
  },
  trailerLink: {
    type: String,
    required: true,
  },
  thumbNail: {
    type: String,
    required: true,
  },
  owner: {
    type: mongoose.Schema.Types.ObjectId,
    ref: 'user',
    required: true,
  },
  movieId: {
    type: Number,
    required: true,
  },
  nameRU: {
    type: String,
    required: true,
  },
  nameEN: {
    type: String,
    required: true,
  },
});

function validateURL(value) {
  const urlRegex = /(ftp|http|https):\/\/(\w+:{0,1}\w*@)?(\S+)(:[0-9]+)?(\/|\/([\w#!:.?+=&%@!\-/]))?/;
  return urlRegex.test(value);
}

movieSchema.path('image').validate((value) => validateURL(value), 'Invalid image URL.');

movieSchema.path('trailerLink').validate((value) => validateURL(value), 'Invalid trailer link URL.');

movieSchema.path('thumbNail').validate((value) => validateURL(value), 'Invalid thumbnail URL.');

module.exports = mongoose.model('movie', movieSchema);
